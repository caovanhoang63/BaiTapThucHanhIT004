CREATE TRIGGER trg_kiemtra_nghd_ngdk ON HOADON
FOR INSERT, UPDATE
    AS
    BEGIN
        DECLARE @ngayhd DATE
        DECLARE @ngaydk DATE
        DECLARE @makh CHAR(4)

        SELECT @makh,@ngayhd = MAKH,NGHD FROM INSERTED
        SELECT @ngaydk = NGDK FROM KHACHHANG WHERE MAKH = @makh

        IF @ngaydk < @ngayhd
        BEGIN
            PRINT N'Ngày tạo hóa đơn không thể bé hơn ngày khách hàng đăng ký'
            ROLLBACK TRAN
        END
    END
    GO;

