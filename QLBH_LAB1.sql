CREATE TRIGGER trg_update_trigia_hoadon ON HOADON
FOR UPDATE
AS
BEGIN
    DECLARE @trigia MONEY
    DECLARE @tongcthd MONEY
    DECLARE @sohd INT
    SELECT @sohd = SOHD FROM INSERTED
    SELECT @trigia = TRIGIA FROM INSERTED
    SELECT @tongcthd = SUM(TONGCTHD)
                    FROM
                    ( SELECT
                        C.SOHD,
                        GIA*SL AS TONGCTHD
                    FROM
                    SANPHAM SP INNER JOIN CTHD C on SP.MASP = C.MASP) as ST WHERE ST.SOHD = @sohd
    IF @trigia <> @tongcthd
    BEGIN
        ROLLBACK TRAN
    END
END
GO
